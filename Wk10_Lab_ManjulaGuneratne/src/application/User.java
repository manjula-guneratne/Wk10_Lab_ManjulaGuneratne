package application;

public class User {

	private String fullName;
	private String currentAddress;
	private String contactNumber;
	private String emailAddress;
	private String highestEducation;
	private String gender;
	private String dateAvailable;
	private String desiredPos;
	private String desiredSalary;
	private String legalWorkAuth;
	private String relWorkingHere;
	private String furtherExplanation;
	
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getCurrentAddress() {
		return currentAddress;
	}
	public void setCurrentAddress(String currentAddress) {
		this.currentAddress = currentAddress;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public String getEmailAddress() {
		return emailAddress;
	}
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}
	public String getHighestEducation() {
		return highestEducation;
	}
	public void setHighestEducation(String highestEducation) {
		this.highestEducation = highestEducation;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDateAvailable() {
		return dateAvailable;
	}
	public void setDateAvailable(String dateAvailable) {
		this.dateAvailable = dateAvailable;
	}
	public String getDesiredPos() {
		return desiredPos;
	}
	public void setDesiredPos(String desiredPos) {
		this.desiredPos = desiredPos;
	}
	public String getDesiredSalary() {
		return desiredSalary;
	}
	public void setDesiredSalary(String desiredSalary) {
		this.desiredSalary = desiredSalary;
	}
	public String getLegalWorkAuth() {
		return legalWorkAuth;
	}
	public void setLegalWorkAuth(String legalWorkAuth) {
		this.legalWorkAuth = legalWorkAuth;
	}
	public String getRelWorkingHere() {
		return relWorkingHere;
	}
	public void setRelWorkingHere(String relWorkingHere) {
		this.relWorkingHere = relWorkingHere;
	}
	public String getFurtherExplanation() {
		return furtherExplanation;
	}
	public void setFurtherExplanation(String furtherExplanation) {
		this.furtherExplanation = furtherExplanation;
	}	
}
